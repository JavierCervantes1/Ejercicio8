    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
        // TODO add your handling code here:
        String res, desc;
        double horas, descu, mult, total;
        
        if (txtHoras.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite por favor el n√∫mero de Horas", "Error", JOptionPane.ERROR_MESSAGE);
            txtHoras.requestFocusInWindow();
        } else {
        
        horas = Double.parseDouble(txtHoras.getText());
        
        mult  = horas * 20000;
        descu = mult * 0.05;
        total = mult - descu;
        
        desc = String.valueOf(descu);
        res  = String.valueOf(total);
        
        txtDesc.setText(desc);
        txtTotal.setText(res);
        }
    }//GEN-LAST:event_cmdCalcularActionPerformed

